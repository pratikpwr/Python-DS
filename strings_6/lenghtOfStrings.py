# length of string can be calculated by len()
name = input("Enter string: ")

length_of_string = len(name)

print("string:", name)
print("length of string:", length_of_string)

# max and min of String or others

maxi = max(name)
mini = min(name)

print("maximum:", maxi + "\nminimum:", mini)

# slicing a String
print(name[0:3])
